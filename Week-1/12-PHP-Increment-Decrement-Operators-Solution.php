    /*****************************************************
     * NOTE:
     * Notice the effect of the order of operation.
     * As the values are modified, the code following
     * the modification uses the new value.
     *****************************************************/
    //Create a variable that is assigned the integer 1
    $x = 1;

    /*****************************************************
     * 1. Create an echo statement that will display the current value of the variable and then increment it up by one
     * 2. Add a line break
     * 3. Create an echo statement to show the new value of the variable
     * 4. Add another line break
     *****************************************************/
    echo $x++ . "<br>" . $x . "<br>";

    /*****************************************************
     * 1. Create an echo statement that will display the value of the variable after it has been incremented up by one
     * 2. Add a line break
     *****************************************************/
    echo ++$x . "<br>";

    /*****************************************************
     * 1. Create an echo statement that will display the current value of the variable and then decrement it down by one
     * 2. Add a line break
     * 3. Create an echo statement to show the new value of the variable
     * 4. Add another line break
     *****************************************************/
    echo $x-- . "<br>" . $x . "<br>";

    /*****************************************************
     * 1. Create an echo statement that will display the value of the variable after it has been decremented down by one
     * 2. Add a line break
     *****************************************************/
    echo --$x . "<br>";

    ?>
